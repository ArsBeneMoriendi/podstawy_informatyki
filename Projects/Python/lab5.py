def func():
    liczba1 = int(input('Podaj pierwszą liczbę: '))
    liczba2 = int(input('Podaj drugą liczbę: '))
    wynik = (liczba1+liczba2)*100
    print(f'Wprowadzone liczby: {liczba1}, {liczba2}')
    print(f'Wynik to {wynik}')   
func()

